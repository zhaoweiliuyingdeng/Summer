package com.zw.pojo;

public class WyViewSpot {

    private String Name;
    private String Introduce;
    public WyViewSpot(String Name,String Introduce)
    {
        this.Name=Name;
        this.Introduce=Introduce;
    }
    public String getName()
    {
        return Name;
    }
    public String getIntroduce()
    {
        return Introduce;
    }
}
